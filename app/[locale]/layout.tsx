import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import { notFound } from "next/navigation";
import { hasLocale, NextIntlClientProvider } from "next-intl";
import { ThemeProvider } from "next-themes";
import ErrorManagerClient from "@/components/common/error_manager_client";
import Header from "@/components/layout/header";
import Footer from "@/components/layout/footer";
import { Toaster } from "@/components/ui/sonner";
import { routing } from "@/lib/i18n/routing";
import { LocaleParams, PageParams } from "@/lib/definitions";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
  params,
}: Readonly<{
  children: React.ReactNode;
  params: PageParams<LocaleParams>;
}>) {
  console.log("params: ", params);
  const { locale } = await params;
  if (!hasLocale(routing.locales, locale)) {
    notFound();
  }

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {/* 国际化 */}
        <NextIntlClientProvider locale={locale}>
          {/* 主题 */}
          <ThemeProvider
            attribute="class"
            enableSystem={false}
            storageKey="VBLAZING_THEME"
          >
            <div className="bg-main-content flex min-h-screen w-full flex-col items-center font-sans sm:items-start">
              <Header />
              <main className="flex w-full flex-1 flex-col items-center sm:items-start">
                {children}
              </main>
              <Footer />
            </div>
            <Toaster position="top-right" />
            <ErrorManagerClient />
          </ThemeProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
